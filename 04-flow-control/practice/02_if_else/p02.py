"""
글로벌시스템융합과 프로그래밍(1) 실습 문제

실습 2: 가위바위보

사용자가 가위, 바위, 보 중 하나를 입력하면
컴퓨터(바위 고정)와 비교하여 결과를 출력하세요.

힌트: if/elif/else를 사용합니다.
"""

computer = "바위"
user = input("가위, 바위, 보 중 하나를 입력하세요: ")

# 아래에 승패를 판정하여 출력하세요
print("컴퓨터: 바위")
print("나:", user)

if user == computer:
    print("비겼습니다")

elif user == "가위":
    print("졌습니다")

elif user == "보":
    print("이겼습니다")

else:
    print("잘못된 입력입니다")

