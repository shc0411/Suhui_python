"""
글로벌시스템융합과 프로그래밍(1) 실습 문제

실습 8: 나만의 파스타 만들기

파스타 주문 시스템입니다.
기본 파스타에 3가지 토핑을 추가할 수 있습니다.

1) 변수 pasta에 "파스타"를 저장하세요.
2) 아래 3개의 input() 결과가 이미 변수에 저장되어 있습니다.
   각 재료에 대해 "예"라고 답했으면,
   pasta 변수에 해당 재료 이름을 이어 붙이세요(+=).
   - add_cheese: "예"이면 pasta에 " + 치즈" 추가
   - add_bacon:  "예"이면 pasta에 " + 베이컨" 추가
   - add_shrimp: "예"이면 pasta에 " + 새우" 추가
3) 최종 pasta 변수를 출력하세요.

힌트: 단일 if문 3개를 연달아 사용합니다. (if/else 아님!)
      문자열 += 연산자를 사용합니다.
"""

add_cheese = input("치즈를 추가할까요? (예/아니오): ")
add_bacon = input("베이컨을 추가할까요? (예/아니오): ")
add_shrimp = input("새우를 추가할까요? (예/아니오): ")

# 아래에 코드를 작성하세요
pasta = "파스타"

if add_cheese == "예":
      pasta = pasta + "+ 치즈"

if add_bacon == "예":
      pasta = pasta + "+ 베이컨"

if add_shrimp == "예":
      pasta = pasta + "+ 새우"

print("나의 파스타: ", pasta)

